        <p class="sinfoniaInfo">The Zeta Chapter of Phi Mu Alpha Sinfonia has enjoyed a long and rich history at the University of Missouri-Columbia and from its beginning, has continued to bring music to and serve Mizzou with pride. The University of Missouri, Zeta’s Alma Mater, was founded in 1839 as the first public university west of the Mississippi; its 1,262 acre campus is not only designated as an official botanic garden, but also is home to 33,000 students, 18 inner colleges and schools, and 20 NCAA Division I sports teams. The University of Missouri is also one of only 60 institutions to be offered membership to the Association of American Universities.<br>

Founded on June 7th, 1907 with a charter signed by Percy Jewett Burrell on May 19th of the same year, Zeta is the first chapter of Phi Mu Alpha Sinfonia founded west of the Mississippi. Zeta is also the second chapter founded at a public university, as well as the second longest continually active chapter in the history of the Fraternity. It was also the first chapter founded at an institution where there was no established school of music. According to early Sinfonian Yearbook’s and Phi Mu Alpha Sinfonia- A Centennial History, our chapter is a “result of passionate connoisseurs of music, but not musicians by profession”. </p>
